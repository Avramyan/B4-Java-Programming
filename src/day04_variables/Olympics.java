package day04_variables;

public class Olympics {

    public static void main(String[]args){

        int year;
        year = 2026;
        System.out.println("China " + year);

        year = 2030;
        System.out.println("France " + year);
        year = 2034;
        System.out.println("Italy " + year);

        int year_;
        year_ = 2027;
        System.out.println("China " + year_);

        year_ = year_ + 4;
        System.out.println("France " + year_);
        year_ = year_ + 4;
        System.out.println("Italy " + year_);

    }
    }
